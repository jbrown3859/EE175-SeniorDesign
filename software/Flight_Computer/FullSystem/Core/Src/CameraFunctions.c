void InitCAM(I2C_HandleTypeDef i2c, SPI_HandleTypeDef spi, uint8_t format) {
	I2C_Write_Camera(i2c, Sensor_addr_write, 0xFF, 0x01);
	//	print_string(uart, "0xFF: ");
	//	print_hex(uart, I2C_Read_Camera(i2c, Sensor_addr_read, 0xFF));
	I2C_Write_Camera(i2c, Sensor_addr_write, 0x12, 0x80);	//reset common control
	HAL_Delay(100);
	if (format == JPEG) {
		//		print_string(uart, "OV2640_JPEG_INIT");
		//		newline(uart);
		I2C_Write_sensor_reg(i2c, OV2640_JPEG_INIT);
		I2C_Write_sensor_reg(i2c, OV2640_YUV422);
		I2C_Write_sensor_reg(i2c, OV2640_JPEG);
		I2C_Write_Camera(i2c, Sensor_addr_write, 0xff, 0x01);
		I2C_Write_Camera(i2c, Sensor_addr_write, 0x15, 0x00);
		I2C_Write_sensor_reg(i2c, OV2640_320x240_JPEG);
	} else {
		I2C_Write_sensor_reg(i2c, OV2640_QVGA);
	}
}

int OV2640_init(I2C_HandleTypeDef i2c, SPI_HandleTypeDef spi, uint8_t format, uint8_t resolution, uint8_t light_mode, uint8_t effects) {
	//Reset CPLD
	SPI_Write_Camera(spi, CLPD_reg, 0x80);
	SPI_Write_Camera(spi, CLPD_reg, 0x00);
	// Check if the ArduCAM SPI bus is OK
	SPI_Write_Camera(spi, test_reg, 0x55);
//	print_string(uart, "test reg: ");
//	print_hex(uart, SPI_Read_Camera(spi, test_reg));
	if (SPI_Read_Camera(spi, test_reg) != 0x55) {
		return 0;
	}
	//change MCU to display LCD
	SPI_Write_Camera(spi, Sensor_interface_timing_reg, 0x00);
	//	  print_string(uart, "Sensor_interface_timing_reg: ");
	//	  print_hex(uart, SPI_Read_Camera(spi, Sensor_interface_timing_reg));
	//check if its the OV2640
	// Check if the camera module type is OV2640
	I2C_Write_Camera(i2c, Sensor_addr_write, 0xFF, 0x01);
	//	  	print_string(uart, "sensor write: ");
	//	  	print_hex(uart, I2C_Read_Camera(i2c, Sensor_addr_read, 0xFF));
	uint8_t vid, pid;
	vid = I2C_Read_Camera(i2c, Sensor_addr_read, OV2640_CHIPID_HIGH);
	pid = I2C_Read_Camera(i2c, Sensor_addr_read, OV2640_CHIPID_LOW);
	if ((vid != 0x26) && ((pid != 0x41) || (pid != 0x42))) {
		return 0;
	}
//	print_string(uart, "vid: ");
//	print_hex(uart, vid);
//	print_string(uart, "pid: ");
//	print_hex(uart, pid);

	InitCAM(i2c, spi, format);
	OV2640_set_JPEG_size(i2c, resolution);
	OV2640_set_Light_Mode(i2c, light_mode);
	OV2640_set_Special_effects(i2c, effects);
	HAL_Delay(100);
	return 1;
}

void start_capture(SPI_HandleTypeDef spi, UART_HandleTypeDef uart) {
	print_string(uart, "Starting image capture...");
	newline(uart);
	SPI_Write_Camera(spi, ARDUCHIP_FIFO, FIFO_CLEAR_MASK);	//flush_fifo
	//print_hex(uart, SPI_Read_Camera(spi, ARDUCHIP_FIFO));
	SPI_Write_Camera(spi, ARDUCHIP_FIFO, FIFO_CLEAR_MASK);	//clear fifo_flag
	//print_hex(uart, SPI_Read_Camera(spi, ARDUCHIP_FIFO));
	SPI_Write_Camera(spi, ARDUCHIP_FIFO, FIFO_START_MASK);	//start capture
	//print_hex(uart, SPI_Read_Camera(spi, ARDUCHIP_FIFO));
	print_string(uart, "Image capture started!");
	newline(uart);
}

int capture_ready(SPI_HandleTypeDef spi) {
	uint8_t temp;
	temp = SPI_Read_Camera(spi, ARDUCHIP_TRIG);
	temp = temp & CAP_DONE_MASK;
	return temp;
}

uint32_t read_fifo_length(SPI_HandleTypeDef spi) {
	uint32_t len1, len2, len3, length = 0;
	len1 = SPI_Read_Camera(spi, FIFO_SIZE1);
	len2 = SPI_Read_Camera(spi, FIFO_SIZE2);
	len3 = SPI_Read_Camera(spi, FIFO_SIZE3) & 0x7f;
	length = ((len3 << 16) | (len2 << 8) | len1) & 0x07fffff;
	return length;
}

int read_image_to_buffer(I2C_HandleTypeDef i2c, SPI_HandleTypeDef spi, UART_HandleTypeDef uart, uint8_t format, uint8_t **buffer, uint32_t *buffer_length) {
	if (!capture_ready(spi)) {
		print_string(uart, "Image capture not ready\r\n");
		return 0;
	} else {
		print_string(uart, "Reading image to buffer...\r\n");
	}

	// Get the image file length
	uint32_t length = read_fifo_length(spi);
	*buffer_length = length;

	if (length >= MAX_FIFO_SIZE) {
		print_string(uart, "length larger than MAX_FIFO_SIZE, buffer length: ");
		print_hex(uart, length);
		return 0;
	}
	if (length == 0) {
		print_string(uart, "length is 0, buffer length: ");
		print_hex(uart, length);
		return 0;
	}

	// create the buffer
	uint8_t *buf = (uint8_t*) malloc(length * sizeof(uint8_t));

	uint8_t temp = 0;

	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_RESET);
	SPI_Transfer_FIFO(spi, BURST_FIFO_READ);

	while (length--) {
		temp = SPI_Transfer_FIFO(spi, 0x00);
		//Read JPEG data from FIFO
		print_hex(uart, temp);
	}

	SPI_Write_Camera(spi, ARDUCHIP_FIFO, FIFO_CLEAR_MASK);

	InitCAM(i2c, spi, format);
	//OV2640_set_JPEG_size(resolution);

	// return the buffer
	*buffer = buf;

	return 1;
}
