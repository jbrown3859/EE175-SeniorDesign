char SPI_Read_Camera(SPI_HandleTypeDef spi, uint8_t address) {
	char data;
	address = address & 0x7F;
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_SET);
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_RESET);
	HAL_SPI_Transmit(&spi, (uint8_t*) &(address), 1, 100);
	HAL_SPI_Receive(&spi, (uint8_t*) &data, 1, 100);
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_SET);
	return data;
}

void Read_SPI_Regs(SPI_HandleTypeDef spi, UART_HandleTypeDef uart, uint8_t start_add, uint8_t end_add) {
	uint8_t i;;
	for(i = start_add; i <= end_add; i++){
		print_hex(uart, i);
		print_string(uart, ": ");
		print_hex(uart, SPI_Read_Camera(spi, i));
		newline(uart);
	}
}

char SPI_Transfer_FIFO(SPI_HandleTypeDef spi, uint8_t address) {
	char data;
	address = address & 0x7F;
	HAL_SPI_TransmitReceive(&spi, (uint8_t*) &(address), (uint8_t*) &data, 1,
			100);
	return data;
}

void SPI_Write_Camera(SPI_HandleTypeDef spi, uint8_t address, uint8_t data) {
	address = address | 0x80;
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_SET);
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_RESET);
	HAL_SPI_Transmit(&spi, (uint8_t*) &(address), 1, 100);
	HAL_SPI_Transmit(&spi, (uint8_t*) &(data), 1, 100);
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_SET);
}

